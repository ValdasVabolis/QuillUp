class Inquiry < ApplicationRecord
  validates :name, presence: true
  validates :email,
    presence: true,
    format: { with: /\A([^@\s]+)@((?:[-a-z0-9]+\.)+[a-z]{2,})\z/i }
  validates :message, presence: true
end
